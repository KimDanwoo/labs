async function main() {
  console.log('🚀 유비쿼터스 학습 데이터 시드 시작...\n');

  // 카테고리 upsert
  const { error: catErr } = await sbAdmin
    .from('categories')
    .upsert(CATEGORY, { onConflict: 'id' });
  if (catErr) {
    console.error('❌ 카테고리 생성 실패:', catErr.message);
    process.exit(1);
  }
  console.log(`✅ 카테고리 생성/업데이트: ${CATEGORY.icon} ${CATEGORY.title}`);

  // 질문 일괄 삽입
  const rows = questions.map((q, i) => ({
    ...q,
    category_id: CATEGORY_ID,
    order_num: i + 1,
    show_in_daily: false,
    show_in_flashcard: false,
  }));

  const { error: qErr } = await sbAdmin
    .from('questions')
    .upsert(rows, { onConflict: 'id' });

  if (qErr) {
    console.error('❌ 질문 삽입 실패:', qErr.message);
    process.exit(1);
  }

  console.log(`✅ ${rows.length}개 질문 삽입 완료 (show_in_daily: false, show_in_flashcard: false)`);
  console.log('\n📡 유비쿼터스 컴퓨팅 학습 데이터 시드 완료!');
}

main().catch(console.error);
