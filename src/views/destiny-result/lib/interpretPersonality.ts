function analyzeTenGodPattern(allGods: TenGod[]): TenGodPattern {
  const groupScore: Record<TenGodGroup, number> = {
    비겁: 0,
    식상: 0,
    재성: 0,
    관성: 0,
    인성: 0,
  };

  for (const god of allGods) {
    groupScore[GROUP_MAP[god]] += 1;
  }

  const { 비겁, 식상, 재성, 관성, 인성 } = groupScore;

  if (비겁 >= 3) return PATTERN_READINGS['비겁과다'];
  if (인성 >= 3) return PATTERN_READINGS['인성과다'];
  if (재성 >= 2 && 관성 >= 2) return PATTERN_READINGS['재관쌍미'];
  if (식상 >= 2 && 재성 >= 2) return PATTERN_READINGS['식상생재'];
  if (관성 >= 2 && 인성 >= 2) return PATTERN_READINGS['관인쌍청'];
  if (식상 >= 2 && 관성 === 0) return PATTERN_READINGS['식상제관'];

  return PATTERN_READINGS['균형'];
}

// ─── 합·충 영향 서술 ──────────────────────────────────────────────────────

function getCombinationNarrative(
  combinations: CombinationAnalysis,
): string | null {
  const hasConflict = combinations.branchConflicts.length > 0;
  const hasCombine =
    combinations.stemCombinations.length > 0 ||
    combinations.branchSixCombinations.length > 0 ||
    combinations.branchTripleCombinations.length > 0;
  const hasPunishment = combinations.branchPunishments.length > 0;

  if (hasConflict && hasCombine) {
    return '사주에 합(合)과 충(沖)이 함께 있어 변화와 안정이 교차하는 역동적인 삶의 패턴이 있어요. 갈등과 화합을 반복하며 성장하고, 그 과정에서 더 깊은 내공을 쌓아가요. 긴장감 속에서도 새로운 인연과 기회가 함께 찾아오는 기운이에요.';
  }
  if (hasConflict) {
    return '사주에 충(沖)의 기운이 있어 변화와 도전이 많은 삶의 흐름이 있어요. 안정보다 역동적인 상황 속에서 성장하는 타입으로, 삶의 굴곡이 오히려 더 강하게 만드는 원동력이 돼요. 변화를 두려워하기보다 새로운 국면의 시작으로 받아들이는 것이 좋아요.';
  }
  if (hasCombine) {
    return '사주에 합(合)의 기운이 있어 인연과 조화를 이끄는 힘이 있어요. 사람과의 만남에서 새로운 기회가 열리고, 협력을 통해 더 큰 가능성을 펼칠 수 있어요. 좋은 인연이 삶의 중요한 전환점을 만들어주는 경우가 많아요.';
  }
  if (hasPunishment) {
    return '사주에 형(刑)의 기운이 있어 인간관계나 특정 상황에서 의도치 않은 마찰이 생기기 쉬워요. 소통과 배려에 특별히 신경 쓰는 것이 관계를 부드럽게 유지하는 열쇠가 돼요.';
  }

  return null;
}

